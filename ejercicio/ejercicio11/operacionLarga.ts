const primerTermino: number = 2;
const segundoTermino: number = 10;
const tercerTermino: number = 5;
const cuartoTermino: number = 2;
const quintoTermino: number = 7;
const sextoTermino: number = 1;
const resultadoComplejo: number = primerTermino + segundoTermino / tercerTermino * cuartoTermino - quintoTermino % sextoTermino;

console.log(`El resultado de 2 + 10 / 5 * 2 - 7 % 1 es ${resultadoComplejo}`);